
CREATE TABLE IF NOT EXISTS usuariosTable (
	IdUsuarioTable INTEGER PRIMARY KEY AUTOINCREMENT,
	UserName TEXT  NOT NULL,
	Pass TEXT NOT NULL,
	NombreCompleto TEXT DEFAULT NULL,
	Movil TEXT DEFAULT NULL,
	Email TEXT DEFAULT NULL

);

CREATE TABLE IF NOT EXISTS firmasPendientes (
	IdFirma INTEGER PRIMARY KEY AUTOINCREMENT,
	IdUsuario TEXT NOT NULL,
	IdTarea TEXT NOT NULL,
    FirmaBLOB BLOB NOT NULL
);

CREATE TABLE IF NOT EXISTS notificacion (
	IdNotificacion INTEGER PRIMARY KEY AUTOINCREMENT,
	Titulo TEXT  NOT NULL,
	Mensaje TEXT  NOT NULL,
    Leido INTEGER DEFAULT NULL,
	TipoDocumento TEXT NOT NULL,
    Fecha TEXT NOT NULL,
	Ruta TEXT NOT NULL,
	Icono TEXT NOT NULL
);

CREATE TABLE IF NOT EXISTS centrosFavoritos (
	IdCentroFav INTEGER PRIMARY KEY AUTOINCREMENT,
	Id INTEGER  NOT NULL,
	Direccion TEXT  DEFAULT NULL,
    DireccionCompleto TEXT  DEFAULT NULL,
    Nombre TEXT  DEFAULT NULL,
    Localidad TEXT  DEFAULT NULL,
    Provincia TEXT  DEFAULT NULL,
    CodigoPostal TEXT  DEFAULT NULL,
    Telefono TEXT  DEFAULT NULL,
    Email TEXT  DEFAULT NULL,
    Imagen TEXT  DEFAULT NULL,
    Latitud INTEGER DEFAULT 0,
    Longitud INTEGER DEFAULT 0,
    Distancia TEXT  DEFAULT NULL,
    Horario TEXT  DEFAULT NULL
);