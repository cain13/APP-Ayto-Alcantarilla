
CREATE TABLE IF NOT EXISTS usuariosTable (
	IdUsuarioTable INTEGER PRIMARY KEY AUTOINCREMENT,
	UserName TEXT  NOT NULL,
	Pass TEXT NOT NULL,
	IdEmpleado INTEGER NOT NULL,
	HorasSemanales REAL DEFAULT NULL,
	NombreCompleto TEXT DEFAULT NULL,
	Telefono TEXT DEFAULT NULL,
	Email TEXT DEFAULT NULL,
	TomarLocalizacion BOOLEAN DEFAULT NULL,
	EsAdministrador BOOLEAN DEFAULT NULL
);

CREATE TABLE IF NOT EXISTS enviosPendientesTable (
	IdEnvioPendiente INTEGER PRIMARY KEY AUTOINCREMENT,
	UrlEnvio TEXT NOT NULL,
	Contenido TEXT NOT NULL,
	TipoJsonPendiente TEXT NOT NULL
);

CREATE TABLE IF NOT EXISTS notificacionTable (
	IdNotificacion INTEGER PRIMARY KEY AUTOINCREMENT,
	IdNotificacionAPI INTEGER NOT NULL,
	Titulo TEXT DEFAULT NULL,
	Mensaje TEXT DEFAULT NULL,
    Leido BOOLEAN DEFAULT NULL,
    Fecha TEXT NOT NULL,
	Ruta TEXT NOT NULL,
	Icono TEXT NOT NULL
);

CREATE TABLE IF NOT EXISTS incidenciasTable (
	IdIncidencia INTEGER PRIMARY KEY AUTOINCREMENT,
	IdIncidenciaAPI INTEGER  NOT NULL,
	NombreIncidencia TEXT  DEFAULT NULL
);